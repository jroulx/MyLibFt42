#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		k;

	if (!s1 || !s2)
		return (0);
	k = 0;
	while (s1[k] && s2[k] && (s1[k] == s2[k]))
		k++;
	if (!(s1[k]) && !(s2[k]))
		return (1);
	return (0);
}
